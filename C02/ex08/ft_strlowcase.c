char	*ft_strlowcase(char *str)
{
	int		i;
	char	temp;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 64 && str[i] < 91)
		{
			temp = str[i] + 32;
			str[i] = temp;
		}
		i++;
	}
	return (str);
}
