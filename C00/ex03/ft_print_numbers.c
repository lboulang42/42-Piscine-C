#include <unistd.h>

void	ft_print_numbers(void)
{	
	int		i;
	int		j;
	char	c;

	i = 48;
	j = 57;
	while (i <= j)
	{	
		c = i;
		write(1, &c, 1);
		i++;
	}
}
