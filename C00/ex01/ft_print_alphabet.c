#include <unistd.h>

void	ft_print_alphabet(void)
{
	int		i;
	int		j;
	char	c1;

	i = 97;
	j = 122;
	while (i <= j)
	{	
		c1 = i;
		write(1, &c1, 1);
		i++;
	}
}
